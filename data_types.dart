void main() {
  // Data types
  // Integer, Strings, Booleans, Double, Lists, Maps, Dynamic

  // String
  String firstName = "Lolo";
  print("String: $firstName");

  // Integers
  int myNum = 99;
  print("Integer: $myNum");

  // Doubles
  double decimal_number = 3.5;
  print('Double: $decimal_number');

  // Booleans
  bool myBool = false;
  print("Boolean: $myBool");

  // Dynamic
  dynamic fullName = 'Mesakh Lolo';
  print("Dynamic: $fullName");
}
