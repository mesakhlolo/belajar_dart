void main() {
  // Variables

  // Strings
  // var nama = "lolo";
  String nama = "lolo";
  print(nama);

  // Integer
  // var angka = 98;
  int angka = 98;
  double angka_desimal = 4.3;
  print(angka);
  print(angka_desimal);

  // Dynamic
  dynamic firstName = 'Mesakh'; // you can stick using var
  print(firstName);

  // Const and Final
  const String fullName = 'Mesakh Lolo Meha'; // compile time
  final String nickName = 'Loelow'; // running time
  print(fullName);
  print(nickName);

  // declaring and not using
  var myName;
  print(myName);

  myName = 'Lolo';
  print(myName);
}
